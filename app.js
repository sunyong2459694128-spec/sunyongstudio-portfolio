document.addEventListener("click", (event) => {
  const nav = event.target.closest("[data-case-nav]");
  if (nav) {
    const id = nav.dataset.caseNav;
    const direction = Number(nav.dataset.direction);
    updateCaseSlide(id, (caseSlideState[id] || 0) + direction);
    return;
  }

  const dot = event.target.closest("[data-case-dot]");
  if (dot) {
    updateCaseSlide(dot.dataset.caseDot, Number(dot.dataset.slide));
    return;
  }

  const media = event.target.closest("[data-case-media]");
  if (media && !event.target.closest("button")) {
    openMedia(media.dataset.caseMedia, Number(media.dataset.slide));
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key !== "Enter") return;
  const media = event.target.closest("[data-case-media]");
  if (media) {
    openMedia(media.dataset.caseMedia, Number(media.dataset.slide));
  }
});

