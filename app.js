function setupAmbientPointer() {
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const cursorGlow = document.createElement("div");
  cursorGlow.className = "cursor-glow";
  document.body.appendChild(cursorGlow);

  const updatePointer = (event) => {
    cursorState.targetX = event.clientX;
    cursorState.targetY = event.clientY;
    cursorState.active = true;
    document.documentElement.style.setProperty("--cursor-scale", "1");
  };

  const quietPointer = () => {
    cursorState.active = false;
    document.documentElement.style.setProperty("--cursor-scale", "0.78");
  };

  window.addEventListener("pointermove", updatePointer, { passive: true });
  window.addEventListener("pointerleave", quietPointer);
  window.addEventListener("blur", quietPointer);

  let pointerFrame = 0;
  const tick = () => {
    cursorState.x += (cursorState.targetX - cursorState.x) * 0.12;
    cursorState.y += (cursorState.targetY - cursorState.y) * 0.12;

    const centerX = window.innerWidth / 2;
    const centerY = window.innerHeight / 2;
    const parallaxX = ((cursorState.x - centerX) / Math.max(centerX, 1)) * 18;
    const parallaxY = ((cursorState.y - centerY) / Math.max(centerY, 1)) * 18;

    document.documentElement.style.setProperty("--cursor-x", `${cursorState.x}px`);
    document.documentElement.style.setProperty("--cursor-y", `${cursorState.y}px`);
    document.documentElement.style.setProperty("--parallax-x", `${parallaxX.toFixed(2)}px`);
    document.documentElement.style.setProperty("--parallax-y", `${parallaxY.toFixed(2)}px`);
    cursorGlow.style.opacity = cursorState.active ? "0.76" : "0.34";

    const moving = Math.abs(cursorState.targetX - cursorState.x) + Math.abs(cursorState.targetY - cursorState.y) > 0.5;
    if (cursorState.active || moving) {
      pointerFrame = window.requestAnimationFrame(tick);
    } else {
      pointerFrame = 0;
    }
  };

  window.addEventListener(
    "pointermove",
    () => {
      if (!pointerFrame) pointerFrame = window.requestAnimationFrame(tick);
    },
    { passive: true }
  );

  pointerFrame = window.requestAnimationFrame(tick);
}

function refreshPageTargets() {
  pageTargets = [
    ...document.querySelectorAll("main > .scene-section:not(.works), .works, .case-showcase")
  ].filter((section) => section.getBoundingClientRect().height > 0);

  pageTargets.forEach((section) => section.classList.add("page-target"));
  updateCurrentPage();
}

function updateCurrentPage() {
  if (!pageTargets.length) return;

  const viewportAnchor = window.innerHeight * 0.38;
  let nearestIndex = 0;
  let nearestDistance = Infinity;

  pageTargets.forEach((section, index) => {
    const rect = section.getBoundingClientRect();
    const distance = Math.abs(rect.top - viewportAnchor);
    if (distance < nearestDistance) {
      nearestDistance = distance;
      nearestIndex = index;
    }
  });

  currentPageIndex = nearestIndex;
  pageTargets.forEach((section, index) => {
    section.classList.toggle("page-current", index === nearestIndex);
  });
}

function goToPage(index) {
  if (!pageTargets.length) return;

  const nextIndex = Math.max(0, Math.min(index, pageTargets.length - 1));
  const target = pageTargets[nextIndex];
  if (!target) return;

  currentPageIndex = nextIndex;
  pageSwitching = true;
  document.body.classList.add("is-page-switching");
  pageTargets.forEach((section, sectionIndex) => {
    section.classList.toggle("page-current", sectionIndex === nextIndex);
  });

  const targetTop = target.getBoundingClientRect().top + window.scrollY;
  window.scrollTo({ top: Math.max(targetTop, 0), behavior: "smooth" });

  window.clearTimeout(pageSwitchTimer);
  pageSwitchTimer = window.setTimeout(() => {
    pageSwitching = false;
    document.body.classList.remove("is-page-switching");
    updateCurrentPage();
  }, 920);
}

function shouldIgnorePageWheel(event) {
  if (window.innerWidth <= 720) return true;
  if (document.body.classList.contains("dialog-open")) return true;
  if (Math.abs(event.deltaX) > Math.abs(event.deltaY)) return true;
  if (Math.abs(event.deltaY) < 16) return true;
  if (event.ctrlKey || event.metaKey || event.shiftKey) return true;

  const target = event.target;
  if (!(target instanceof Element)) return false;
  return Boolean(target.closest("input, textarea, select, .case-dialog, .channel-bar"));
}

function setupPageSwitcher() {
  if (pageSwitcherReady) return;
  pageSwitcherReady = true;
  refreshPageTargets();

  window.addEventListener(
    "wheel",
    (event) => {
      if (shouldIgnorePageWheel(event)) return;
      event.preventDefault();
      if (pageSwitching) return;
      goToPage(currentPageIndex + (event.deltaY > 0 ? 1 : -1));
    },
    { passive: false }
  );

  window.addEventListener("keydown", (event) => {
    if (document.body.classList.contains("dialog-open")) return;
    if (event.target instanceof Element && event.target.closest("input, textarea, select")) return;

    const nextKeys = ["ArrowDown", "PageDown", " "];
    const prevKeys = ["ArrowUp", "PageUp"];
    if (![...nextKeys, ...prevKeys].includes(event.key)) return;

    event.preventDefault();
    if (pageSwitching) return;
    goToPage(currentPageIndex + (nextKeys.includes(event.key) ? 1 : -1));
  });

  window.addEventListener("scroll", () => {
    if (!pageSwitching) window.requestAnimationFrame(updateCurrentPage);
  }, { passive: true });
}

function getMediaMeta(src) {
  return mediaMeta[src] || { ratio: "16 / 9", orientation: "landscape" };
}

function registerImageMeta(src, width, height) {
  if (!src || !width || !height) return;
  const ratioValue = width / height;
  mediaMeta[src] = {
    width,
    height,
    ratio: `${width} / ${height}`,
    orientation:
      ratioValue > 1.35 ? "landscape" : ratioValue < 0.55 ? "longform" : ratioValue < 0.8 ? "portrait" : "balanced"
  };
}

function preloadMediaMeta() {
  const imageSources = [...new Set(works.flatMap(projectMedia).filter((src) => src && !src.endsWith(".mp4")))];
  imageSources.forEach((src) => {
    const image = new Image();
    image.onload = () => {
      registerImageMeta(src, image.naturalWidth, image.naturalHeight);
      renderCaseSections();
      fitCaseFrames();
    };
    image.src = src;
  });
}

function projectMedia(work) {
  const media = [work.cover, ...work.gallery].filter(Boolean);
  return [...new Set(media)];
}

function mediaMarkup(src, alt, lazy = true) {
  if (!src) {
    return `<div class="placeholder-art" aria-label="${alt} 占位封面"><span>${alt}</span></div>`;
  }

  const loading = lazy ? ' loading="lazy"' : "";
  if (src.endsWith(".mp4")) {
    return `<video src="${src}" muted autoplay loop playsinline></video>`;
  }
  return `<img src="${src}" alt="${alt}"${loading} />`;
}

function mediaBackdropMarkup(src, alt) {
  if (!src || src.endsWith(".mp4")) return "";
  return `<div class="dialog-hero-backdrop" aria-hidden="true">${mediaMarkup(src, `${alt} 背景`, false)}</div>`;
}

function getFilteredWorks() {
  const normalizedKeyword = keyword.trim().toLowerCase();
  return works.filter((work) => {
    const filterMatched = activeFilter === "全部" || work.category === activeFilter;
    const keywordMatched = [work.title, work.category, work.role, work.summary]
      .join(" ")
      .toLowerCase()
      .includes(normalizedKeyword);
    return filterMatched && keywordMatched;
  });
}

function carouselItem(work, media, position, label) {
  const src = media.length ? media[position] : "";
  const meta = getMediaMeta(src);
  return `
    <div class="case-slide ${label} ${meta.orientation}" style="--media-ratio: ${meta.ratio}" role="button" tabindex="0" data-case-media="${work.id}" data-slide="${position}" data-src="${src}">
      ${mediaMarkup(src, `${work.title} ${label}`)}
    </div>
  `;
}

